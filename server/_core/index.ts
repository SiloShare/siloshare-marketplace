import "dotenv/config";
import express from "express";
import { createServer } from "http";
import net from "net";
import { createExpressMiddleware } from "@trpc/server/adapters/express";
import rateLimit from "express-rate-limit";
import { registerOAuthRoutes } from "./oauth";
import { appRouter } from "../routers";
import { createContext } from "./context";
import { serveStatic, setupVite } from "./vite";

function isPortAvailable(port: number): Promise<boolean> {
  return new Promise(resolve => {
    const server = net.createServer();
    server.listen(port, () => {
      server.close(() => resolve(true));
    });
    server.on("error", () => resolve(false));
  });
}

async function findAvailablePort(startPort: number = 3000): Promise<number> {
  for (let port = startPort; port < startPort + 20; port++) {
    if (await isPortAvailable(port)) {
      return port;
    }
  }
  throw new Error(`No available port found starting from ${startPort}`);
}

async function startServer() {
  const app = express();
  const server = createServer(app);
  
  // Trust proxy - necess√°rio para rate limiting e cookies em produ√ß√£o
  app.set('trust proxy', 1);
  
  // Logging middleware
  app.use((req, res, next) => {
    const timestamp = new Date().toISOString();
    console.log(`[${timestamp}] ${req.method} ${req.path}`);
    next();
  });
  
  // Rate limiting - Geral (100 requisi√ß√µes por 15 minutos)
  const generalLimiter = rateLimit({
    windowMs: 15 * 60 * 1000, // 15 minutos
    max: 100, // 100 requisi√ß√µes por IP
    message: 'Muitas requisi√ß√µes deste IP, tente novamente em 15 minutos',
    standardHeaders: true,
    legacyHeaders: false,
  });
  
  // Rate limiting - Autentica√ß√£o (10 tentativas por 15 minutos)
  const authLimiter = rateLimit({
    windowMs: 15 * 60 * 1000, // 15 minutos
    max: 10, // 10 tentativas de login/cadastro
    standardHeaders: true,
    legacyHeaders: false,
    skipSuccessfulRequests: true, // N√£o conta requisi√ß√µes bem-sucedidas
    handler: (req, res) => {
      res.status(429).json({
        error: {
          message: 'Muitas tentativas de login/cadastro, tente novamente em 15 minutos',
          code: 'TOO_MANY_REQUESTS'
        }
      });
    }
  });
  
  app.use('/api', generalLimiter);
  app.use('/api/trpc/auth.login', authLimiter);
  app.use('/api/trpc/auth.register', authLimiter);
  app.use('/api/trpc/auth.forgotPassword', authLimiter);
  app.use('/api/trpc/auth.resetPassword', authLimiter);
  
  console.log('‚úÖ Rate limiting configurado');
  console.log('  - Geral: 100 req/15min');
  console.log('  - Auth: 10 tentativas/15min');
  
  // Configure body parser with larger size limit for file uploads
  app.use(express.json({ limit: "50mb" }));
  app.use(express.urlencoded({ limit: "50mb", extended: true }));
  
  // Health check endpoint
  app.get("/api/health", (req, res) => {
    res.json({ 
      status: "ok", 
      timestamp: new Date().toISOString(),
      env: process.env.NODE_ENV || "development"
    });
  });
  // OAuth callback under /api/oauth/callback
  registerOAuthRoutes(app);
  
  // Upload router
  const uploadRouter = await import("../upload");
  app.use("/api", uploadRouter.default);
  // tRPC API
  app.use(
    "/api/trpc",
    createExpressMiddleware({
      router: appRouter,
      createContext,
    })
  );
  // development mode uses Vite, production mode uses static files
  if (process.env.NODE_ENV === "development") {
    await setupVite(app, server);
  } else {
    serveStatic(app);
  }

  const preferredPort = parseInt(process.env.PORT || "3000");
  const port = await findAvailablePort(preferredPort);

  if (port !== preferredPort) {
    console.log(`Port ${preferredPort} is busy, using port ${port} instead`);
  }

  server.listen(port, () => {
    console.log(`\nüöÄ Server running on http://localhost:${port}/`);
    console.log(`üì° tRPC API: http://localhost:${port}/api/trpc`);
    console.log(`‚ù§Ô∏è  Health check: http://localhost:${port}/api/health`);
    console.log(`üîß Environment: ${process.env.NODE_ENV || "development"}\n`);
  });
}

startServer().catch(console.error);
